package day01_Variables;
public class C01_Variables {
    public static void main(String[] args) {
		
		//variable olusturmak ve deger atamak farkli islemler
		//varible olusturma ve deger atamayi tek satirda yapabiliriz
		
		String studentName = "Burak"; 
		System.out.println(studentName); //Burak
		
		//istersek de once olusturur, sonra da deger atayabiliriz
		int num;
		num=10;
		System.out.println(num); //10
		
		//daha once olusturulan ve deger atamasi yapilan bir degiskene yeni bir deger atayabiliriz
		//java yeni atadigimiz degeri kabul eder
		num=25;
		System.out.println(num); //25
		
		studentName="Sahin";
		System.out.println(studentName); //Sahin
	}
}
